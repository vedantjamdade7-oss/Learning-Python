# Grading system

score = float(input("Enter your marks :"))
if score >= 90:
    print("Grade - A+ You are pass")
elif score >= 80:
    print("Grade - A You are pass")
elif score >= 70:
    print("Grade - B+ you are pass")
elif score >= 60:
    print("Grade - B you are pass")
elif score >= 50:
    print("Grade - c you are pass")
elif score >= 40:
    print("Grade - D you are pass")
elif score >= 35:
    print("Grade - E you are pass")
else :
    print("Grade - F You are Fail !")