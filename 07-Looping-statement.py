# Looping Statements (Iteration)

# Used to repeat code multiple times.

# Types:

# 🔹 for Loop
# Used when you know how many times the loop should run.

# 🔹 while Loop
# Used when the loop runs until a condition becomes false.

# 🔹 Nested Loop
# An loop inside another loop.

# 🔹 Loop Control Statements
# Used to control the flow of the loop.

# 1. Break Statement
# Used to exit the loop prematurely.

# 2. Continue Statement
# Used to skip the current iteration and continue with the next one.

# 3. Pass statement
# Used as a placeholder for future code. It does nothing when executed.