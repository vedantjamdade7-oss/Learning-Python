# Conditiontnal stateme in python

# Conditional statements allow a program to make decisions based on conditions.
# They execute different blocks of code depending on whether a condition is True or False.

# 1. if Statement
# The if statement executes code only if the condition is true.

# 2. if-else Statement
# The if-else statement executes one block if the condition is True and another if it is False.

# 3. if-elif-else Statement
# Used when checking multiple conditions.

# 4. Nested if Statement
# An if statement inside another if statement.

